# Activity 1: Compare numbers
print("\n=== Activity 1: Number Comparison ===")
x = 25
y = 30
print("x =", x, ", y =", y)
print("x < y:", x < y)
print("x > y:", x > y)
print("x == y:", x == y)
print("x != y:", x != y)

# Activity 2: Compare strings
print("\n=== Activity 2: String Comparison ===")
name1 = "Alice"
name2 = "Bob"
print("name1 =", name1, ", name2 =", name2)
print("name1 == name2:", name1 == name2)
print("name1 != name2:", name1 != name2)
print("name1 == 'Alice':", name1 == "Alice")

# Activity 3: Chain of comparisons
print("\n=== Activity 3: Multiple Comparisons ===")
score1 = 85
score2 = 90
score3 = 78
print("score1 =", score1, ", score2 =", score2, ", score3 =", score3)
print("score1 < score2:", score1 < score2)
print("score2 > score1 and score2 > score3:", score2 > score1 and score2 > score3)
print("score1 == score2 or score1 == score3:", score1 == score2 or score1 == score3)
print("score3 <= score1:", score3 <= score1)
